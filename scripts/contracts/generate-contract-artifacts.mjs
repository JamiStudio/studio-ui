// A suite registry item ships an install-graph manifest as its single file. The
// manifest is generated from the source item so the suite descriptor cannot
// drift from the item graph it claims to install.
function suiteManifest(item) {
  const shell = readOptionalJson(`registry/suites/${item.suite}/suite-shell.json`);
  const registryDependencies = suiteRegistryDependencies(item);
  return {
    $schema: "https://jami.studio/schemas/registry/suite-manifest.generated.json",
    name: item.name,
    lane: item.suite ?? null,
    schemaVersion: item.lifecycle.schemaVersion,
    items: registryDependencies,
    installGraph: {
      root: item.name.replace("@jami-studio/", ""),
      dependencies: registryDependencies,
    },
    plannedSurfaces: item.plannedSurfaces ?? [],
    shell: shell
      ? {
          sourceSchemaVersion: shell.schemaVersion,
          title: shell.title,
          summary: shell.summary,
          appShell: shell.appShell,
          routes: shell.routes ?? [],
          pages: shell.pages ?? [],
          blocks: shell.blocks ?? [],
          componentParts: [...new Set((shell.pages ?? []).flatMap((page) => page.components ?? []))].sort(),
          stateFixtures: shell.stateFixtures ?? {},
          provenance: {
            source: sourceRef(item.suite),
            copiedSource: false,
          },
        }
      : null,
    implementation: shell
      ? {
          status: "renderable-primitive-factory-implementation",
          runtimeReactApp: false,
          app: appImplementationFile(item.suite).target,
          pages: (shell.pages ?? []).map((page) => pageImplementationFile(item.suite, page).target),
          blocks: (shell.blocks ?? []).map((block) => blockImplementationFile(item.suite, block).target),
        }
      : null,
  };
}

function suitePartSlug(id, prefix) {
  return String(id ?? "")
    .replace(prefix, "")
    .replaceAll(".", "-");
}

