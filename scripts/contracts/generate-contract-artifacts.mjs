import { createHash } from "node:crypto";
import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = process.cwd();

const tokenSourcePath = "packages/tokens/fixtures/valid/jami-factory.tokens.json";
const registrySourceDir = "packages/registry/fixtures/valid";

function isObject(value) {
  return value !== null && typeof value === "object" && !Array.isArray(value);
}

function readJson(path) {
  return JSON.parse(readFileSync(join(root, path), "utf8"));
}

function readOptionalJson(path) {
  const absolute = join(root, path);
  return existsSync(absolute) ? JSON.parse(readFileSync(absolute, "utf8")) : null;
}

function flattenTokens(node, prefix = [], out = new Map()) {
  if (!isObject(node)) return out;
  for (const [key, value] of Object.entries(node)) {
    if (key.startsWith("$")) continue;
    if (isObject(value) && "$value" in value) {
      out.set([...prefix, key].join("."), value);
    } else if (isObject(value)) {
      flattenTokens(value, [...prefix, key], out);
    }
  }
  return out;
}

function resolveTokenValue(tokenMap, ref, seen = new Set()) {
  if (seen.has(ref)) throw new Error(`circular token reference: ${[...seen, ref].join(" -> ")}`);
  const token = tokenMap.get(ref);
  if (!token) throw new Error(`missing token reference: ${ref}`);
  const value = token.$value;
  if (typeof value === "string") {
    const match = value.match(/^\{([^}]+)\}$/);
    if (match) return resolveTokenValue(tokenMap, match[1], new Set([...seen, ref]));
  }
  return value;
}

function tokenVarName(name) {
  return `--jami-${name.replaceAll(".", "-")}`;
}

function cssValue(value) {
  if (isObject(value)) return JSON.stringify(value);
  return String(value);
}

function toCssVariables(tokenMap) {
  const lines = [
    "/* Generated by scripts/contracts/generate-contract-artifacts.mjs. Do not edit by hand. */",
    ":root {",
  ];
  for (const [name] of tokenMap) {
    lines.push(`  ${tokenVarName(name)}: ${cssValue(resolveTokenValue(tokenMap, name))};`);
  }
  lines.push("}");
  return `${lines.join("\n")}\n`;
}

function toTailwindTheme(tokenMap) {
  const lines = [
    "/* Generated by scripts/contracts/generate-contract-artifacts.mjs. Do not edit by hand. */",
    "@theme {",
  ];
  for (const [name] of tokenMap) {
    lines.push(`  ${tokenVarName(name)}: var(${tokenVarName(name)});`);
  }
  lines.push("}");
  return `${lines.join("\n")}\n`;
}

function toTypescriptTypes(tokenMap, schemaVersion) {
  const tokenNames = [...tokenMap.keys()];
  const lines = [
    "// Generated by scripts/contracts/generate-contract-artifacts.mjs. Do not edit by hand.",
    `export const tokenSchemaVersion = ${JSON.stringify(schemaVersion)} as const;`,
    "",
    "export const tokenNames = [",
    ...tokenNames.map((name) => `  ${JSON.stringify(name)},`),
    "] as const;",
    "",
    "export type JamiTokenName = (typeof tokenNames)[number];",
    "",
    "export const tokenCssVariables: Record<JamiTokenName, string> = {",
    ...tokenNames.map((name) => `  ${JSON.stringify(name)}: ${JSON.stringify(`var(${tokenVarName(name)})`)},`),
    "};",
  ];
  return `${lines.join("\n")}\n`;
}

function toShadcnCssVars(tokenMap, schemaVersion) {
  const semantic = {};
  for (const mode of ["light", "dark"]) {
    semantic[mode] = {
      background: `var(${tokenVarName(`semantic.${mode}.background`)})`,
      foreground: `var(${tokenVarName(`semantic.${mode}.foreground`)})`,
      accent: `var(${tokenVarName(`semantic.${mode}.accent`)})`,
      ring: `var(${tokenVarName("componentState.focusRing")})`,
    };
  }
  return `${JSON.stringify(
    {
      $schema: "https://jami.studio/schemas/tokens/shadcn-css-vars.generated.json",
      schemaVersion,
      cssVars: semantic,
    },
    null,
    2,
  )}\n`;
}

function compareOrWrite(path, contents, { check }) {
  const absolutePath = join(root, path);
  if (check) {
    if (!existsSync(absolutePath)) return [`${path}: missing generated artifact`];
    const current = readFileSync(absolutePath, "utf8");
    return current === contents ? [] : [`${path}: generated artifact drift`];
  }
  mkdirSync(dirname(absolutePath), { recursive: true });
  writeFileSync(absolutePath, contents);
  return [];
}

export function registrySourceHash(item) {
  const normalized = structuredClone(item);
  if (normalized.lifecycle) normalized.lifecycle.sourceHash = "";
  return `sha256:${createHash("sha256").update(`${JSON.stringify(normalized, null, 2)}\n`).digest("hex")}`;
}

function contentHash(content) {
  return `sha256:${createHash("sha256").update(content).digest("hex")}`;
}

// Embed real install content + a content hash for any file whose source path
// resolves on disk (generated token outputs, generated suite manifests). Files
// whose source does not exist yet (for example a primitive whose `.tsx` source
// is pending Workstream 4) carry no content: the CLI reports them as
// source-pending rather than fabricating an installable file.
function registryOutputFile(file) {
  const out = { path: file.path, target: file.target, type: file.kind };
  const absolute = join(root, file.path);
  if (existsSync(absolute)) {
    const content = readFileSync(absolute, "utf8");
    out.content = content;
    out.hash = contentHash(content);
  }
  return out;
}

function registryOutputItem(item) {
  const registryDependencies =
    item.type === "suite" ? suiteRegistryDependencies(item) : item.registryDependencies ?? [];
  return {
    name: item.name.replace("@jami-studio/", ""),
    type: item.type === "primitive" ? "registry:ui" : `registry:${item.type}`,
    title: item.title,
    description: item.description,
    dependencies: item.dependencies,
    registryDependencies,
    files: item.files.map(registryOutputFile),
    meta: {
      packageName: item.name,
      suite: item.suite ?? null,
      lifecycle: item.lifecycle,
      tokenRequirements: item.tokenRequirements,
      provenance: item.provenance,
      compatibility: item.compatibility,
      ...(item.plannedSurfaces ? { plannedSurfaces: item.plannedSurfaces } : {}),
      agentManifest: item.agentManifest ?? null,
    },
  };
}

// A suite registry item ships an install-graph manifest as its single file. The
// manifest is generated from the source item so the suite descriptor cannot
// drift from the item graph it claims to install.
function suiteManifest(item) {
  const shell = readOptionalJson(`registry/suites/${item.suite}/suite-shell.json`);
  const registryDependencies = suiteRegistryDependencies(item);
  return {
    $schema: "https://jami.studio/schemas/registry/suite-manifest.generated.json",
    name: item.name,
    lane: item.suite ?? null,
    schemaVersion: item.lifecycle.schemaVersion,
    items: registryDependencies,
    installGraph: {
      root: item.name.replace("@jami-studio/", ""),
      dependencies: registryDependencies,
    },
    plannedSurfaces: item.plannedSurfaces ?? [],
    shell: shell
      ? {
          sourceSchemaVersion: shell.schemaVersion,
          title: shell.title,
          summary: shell.summary,
          appShell: shell.appShell,
          routes: shell.routes ?? [],
          pages: shell.pages ?? [],
          blocks: shell.blocks ?? [],
          componentParts: [...new Set((shell.pages ?? []).flatMap((page) => page.components ?? []))].sort(),
          stateFixtures: shell.stateFixtures ?? {},
          provenance: {
            source: `registry/suites/${item.suite}/suite-shell.json`,
            copiedSource: false,
          },
        }
      : null,
  };
}

function suitePartSlug(id, prefix) {
  return String(id ?? "")
    .replace(prefix, "")
    .replaceAll(".", "-");
}

function suitePageItemName(lane, page) {
  return `${lane}-${suitePartSlug(page.id, `page.${lane}.`)}-page`;
}

function suiteBlockItemName(lane, block) {
  return `${lane}-${suitePartSlug(block.id, `block.${lane}.`)}-block`;
}

function suiteRegistryDependencies(item) {
  const shell = readOptionalJson(`registry/suites/${item.suite}/suite-shell.json`);
  if (!shell) return item.registryDependencies ?? [];
  const pages = (shell.pages ?? []).map((page) => suitePageItemName(item.suite, page));
  const blocks = (shell.blocks ?? []).map((block) => suiteBlockItemName(item.suite, block));
  return [...new Set([...(item.registryDependencies ?? []), ...pages, ...blocks])];
}

function suitePageManifest(lane, shell, page) {
  const routes = (shell.routes ?? []).filter((route) => route.page === page.id);
  const blockIds = [...new Set(routes.flatMap((route) => route.blocks ?? []))];
  const blocks = blockIds
    .map((blockId) => (shell.blocks ?? []).find((block) => block.id === blockId))
    .filter(Boolean);
  return {
    $schema: "https://jami.studio/schemas/registry/suite-page.generated.json",
    schemaVersion: shell.schemaVersion,
    lane,
    page,
    routes,
    blocks,
    components: [...new Set([...(page.components ?? []), ...blocks.map((block) => block.component)])].sort(),
    stateFixtures: shell.stateFixtures ?? {},
    provenance: {
      source: `registry/suites/${lane}/suite-shell.json`,
      copiedSource: false,
    },
  };
}

function suiteBlockManifest(lane, shell, block) {
  const routes = (shell.routes ?? []).filter((route) => (route.blocks ?? []).includes(block.id));
  const pages = (shell.pages ?? []).filter((page) => routes.some((route) => route.page === page.id));
  return {
    $schema: "https://jami.studio/schemas/registry/suite-block.generated.json",
    schemaVersion: shell.schemaVersion,
    lane,
    block,
    routes: routes.map((route) => ({ path: route.path, page: route.page, title: route.title })),
    pages: pages.map((page) => ({ id: page.id, title: page.title })),
    component: block.component,
    stateFixtures: Object.fromEntries(
      (block.stateExamples ?? []).map((state) => [
        state,
        state === "long-content"
          ? shell.stateFixtures?.longContent
          : state === "empty"
            ? shell.stateFixtures?.empty
            : state === "error"
              ? shell.stateFixtures?.error
              : `${block.title} includes the ${state} state.`,
      ]),
    ),
    provenance: {
      source: `registry/suites/${lane}/suite-shell.json`,
      copiedSource: false,
    },
  };
}

function suitePartItems(suiteItem) {
  const lane = suiteItem.suite;
  const shell = readOptionalJson(`registry/suites/${lane}/suite-shell.json`);
  if (!shell) return [];
  const base = {
    $schema: "https://jami.studio/schemas/registry/registry-item.schema.json",
    suite: lane,
    dependencies: [],
    tokenRequirements: [],
    provenance: {
      source: `registry/suites/${lane}/suite-shell.json`,
      license: "MIT",
      reviewedAt: "2026-06-09",
      copiedSource: false,
    },
    compatibility: suiteItem.compatibility,
  };
  const pageItems = (shell.pages ?? []).map((page) => {
    const name = suitePageItemName(lane, page);
    const routes = (shell.routes ?? []).filter((route) => route.page === page.id);
    const blockDeps = [
      ...new Set(
        routes
          .flatMap((route) => route.blocks ?? [])
          .map((blockId) => {
            const block = (shell.blocks ?? []).find((candidate) => candidate.id === blockId);
            return block ? suiteBlockItemName(lane, block) : null;
          })
          .filter(Boolean),
      ),
    ];
    const item = {
      ...base,
      name: `@jami-studio/${name}`,
      type: "page",
      title: page.title,
      description: page.description,
      registryDependencies: [...new Set([...blockDeps, ...(page.components ?? [])])],
      files: [
        {
          path: `packages/registry/generated/suites/${lane}/pages/${name}.page.json`,
          target: `studio-ui/suites/${lane}/pages/${name}.page.json`,
          kind: "registry:page",
        },
      ],
      lifecycle: {
        id: `page.${lane}.${suitePartSlug(page.id, `page.${lane}.`)}`,
        version: suiteItem.lifecycle.version,
        schemaVersion: suiteItem.lifecycle.schemaVersion,
        sourceHash: "",
        migrationNotes:
          "Generated standalone page descriptor from the authored suite shell; full React page implementation remains pending.",
      },
    };
    item.lifecycle.sourceHash = registrySourceHash(item);
    return item;
  });
  const blockItems = (shell.blocks ?? []).map((block) => {
    const name = suiteBlockItemName(lane, block);
    const item = {
      ...base,
      name: `@jami-studio/${name}`,
      type: "block",
      title: block.title,
      description: `${block.title} block for the ${shell.title} suite shell.`,
      registryDependencies: [block.component],
      files: [
        {
          path: `packages/registry/generated/suites/${lane}/blocks/${name}.block.json`,
          target: `studio-ui/suites/${lane}/blocks/${name}.block.json`,
          kind: "registry:block",
        },
      ],
      lifecycle: {
        id: `block.${lane}.${suitePartSlug(block.id, `block.${lane}.`)}`,
        version: suiteItem.lifecycle.version,
        schemaVersion: suiteItem.lifecycle.schemaVersion,
        sourceHash: "",
        migrationNotes:
          "Generated standalone block descriptor from the authored suite shell; full React block implementation remains pending.",
      },
    };
    item.lifecycle.sourceHash = registrySourceHash(item);
    return item;
  });
  return [...pageItems, ...blockItems];
}

function generateTokenArtifacts({ check }) {
  const source = readJson(tokenSourcePath);
  const tokenMap = flattenTokens(source.tokens);
  const schemaVersion = source.$extensions["studio-ui"].schemaVersion;
  const outputs = source.$extensions["studio-ui"].generatedOutputs;

  return [
    ...compareOrWrite(outputs.cssVariables, toCssVariables(tokenMap), { check }),
    ...compareOrWrite(outputs.tailwindTheme, toTailwindTheme(tokenMap), { check }),
    ...compareOrWrite(outputs.typescriptTypes, toTypescriptTypes(tokenMap, schemaVersion), { check }),
    ...compareOrWrite(outputs.shadcnCssVars, toShadcnCssVars(tokenMap, schemaVersion), { check }),
  ];
}

function generateRegistryArtifacts({ check }) {
  const itemPaths = readdirSync(join(root, registrySourceDir))
    .filter((name) => name.endsWith(".registry-item.json"))
    .sort();
  const sourceItems = itemPaths.map((name) => readJson(`${registrySourceDir}/${name}`));
  const derivedItems = sourceItems.flatMap((item) => (item.type === "suite" ? suitePartItems(item) : []));
  const allItems = [...sourceItems, ...derivedItems].sort((a, b) => a.name.localeCompare(b.name));
  const failures = [];

  // Pass 1: emit suite install-graph manifests so suite items can embed real
  // generated content, plus standalone page/block manifests derived from the
  // authored shell source, before item outputs build.
  for (const item of sourceItems) {
    if (item.type !== "suite") continue;
    const shell = readOptionalJson(`registry/suites/${item.suite}/suite-shell.json`);
    if (shell) {
      for (const page of shell.pages ?? []) {
        const name = suitePageItemName(item.suite, page);
        failures.push(
          ...compareOrWrite(
            `packages/registry/generated/suites/${item.suite}/pages/${name}.page.json`,
            `${JSON.stringify(suitePageManifest(item.suite, shell, page), null, 2)}\n`,
            { check },
          ),
        );
      }
      for (const block of shell.blocks ?? []) {
        const name = suiteBlockItemName(item.suite, block);
        failures.push(
          ...compareOrWrite(
            `packages/registry/generated/suites/${item.suite}/blocks/${name}.block.json`,
            `${JSON.stringify(suiteBlockManifest(item.suite, shell, block), null, 2)}\n`,
            { check },
          ),
        );
      }
    }
    failures.push(
      ...compareOrWrite(
        `packages/registry/generated/suites/${item.suite}.suite.json`,
        `${JSON.stringify(suiteManifest(item), null, 2)}\n`,
        { check },
      ),
    );
  }

  const generatedItems = allItems.map(registryOutputItem);
  const registry = {
    $schema: "https://ui.shadcn.com/schema/registry.json",
    name: "@jami-studio/registry",
    homepage: "https://registry.jami.studio",
    items: generatedItems,
  };
  failures.push(
    ...compareOrWrite("packages/registry/generated/registry.json", `${JSON.stringify(registry, null, 2)}\n`, {
      check,
    }),
  );

  for (const item of generatedItems) {
    failures.push(
      ...compareOrWrite(
        `packages/registry/generated/items/${item.name}.registry-item.json`,
        `${JSON.stringify({ $schema: "https://ui.shadcn.com/schema/registry-item.json", ...item }, null, 2)}\n`,
        { check },
      ),
    );
  }
  return failures;
}

export function generateAllArtifacts({ check = false } = {}) {
  return [...generateTokenArtifacts({ check }), ...generateRegistryArtifacts({ check })];
}

const isDirectRun = process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1];

if (isDirectRun) {
  const check = process.argv.includes("--check");
  const failures = generateAllArtifacts({ check });
  if (failures.length > 0) {
    console.error(check ? "contracts:artifacts:check failed" : "contracts:artifacts:generate failed");
    for (const failure of failures) console.error(`- ${failure}`);
    process.exit(1);
  }
  console.log(check ? "contracts:artifacts:check passed" : "contracts:artifacts:generate wrote artifacts");
}
