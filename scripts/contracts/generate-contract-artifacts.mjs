import { createHash } from "node:crypto";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = process.cwd();

const tokenSourcePath = "packages/tokens/fixtures/valid/jami-factory.tokens.json";
const registrySourceDir = "packages/registry/fixtures/valid";

function isObject(value) {
  return value !== null && typeof value === "object" && !Array.isArray(value);
}

function readJson(path) {
  return JSON.parse(readFileSync(join(root, path), "utf8"));
}

function flattenTokens(node, prefix = [], out = new Map()) {
  if (!isObject(node)) return out;
  for (const [key, value] of Object.entries(node)) {
    if (key.startsWith("$")) continue;
    if (isObject(value) && "$value" in value) {
      out.set([...prefix, key].join("."), value);
    } else if (isObject(value)) {
      flattenTokens(value, [...prefix, key], out);
    }
  }
  return out;
}

function resolveTokenValue(tokenMap, ref, seen = new Set()) {
  if (seen.has(ref)) throw new Error(`circular token reference: ${[...seen, ref].join(" -> ")}`);
  const token = tokenMap.get(ref);
  if (!token) throw new Error(`missing token reference: ${ref}`);
  const value = token.$value;
  if (typeof value === "string") {
    const match = value.match(/^\{([^}]+)\}$/);
    if (match) return resolveTokenValue(tokenMap, match[1], new Set([...seen, ref]));
  }
  return value;
}

function tokenVarName(name) {
  return `--jami-${name.replaceAll(".", "-")}`;
}

function cssValue(value) {
  if (isObject(value)) return JSON.stringify(value);
  return String(value);
}

function toCssVariables(tokenMap) {
  const lines = [
    "/* Generated by scripts/contracts/generate-contract-artifacts.mjs. Do not edit by hand. */",
    ":root {",
  ];
  for (const [name] of tokenMap) {
    lines.push(`  ${tokenVarName(name)}: ${cssValue(resolveTokenValue(tokenMap, name))};`);
  }
  lines.push("}");
  return `${lines.join("\n")}\n`;
}

function toTailwindTheme(tokenMap) {
  const lines = [
    "/* Generated by scripts/contracts/generate-contract-artifacts.mjs. Do not edit by hand. */",
    "@theme {",
  ];
  for (const [name] of tokenMap) {
    lines.push(`  ${tokenVarName(name)}: var(${tokenVarName(name)});`);
  }
  lines.push("}");
  return `${lines.join("\n")}\n`;
}

function toTypescriptTypes(tokenMap, schemaVersion) {
  const tokenNames = [...tokenMap.keys()];
  const lines = [
    "// Generated by scripts/contracts/generate-contract-artifacts.mjs. Do not edit by hand.",
    `export const tokenSchemaVersion = ${JSON.stringify(schemaVersion)} as const;`,
    "",
    "export const tokenNames = [",
    ...tokenNames.map((name) => `  ${JSON.stringify(name)},`),
    "] as const;",
    "",
    "export type JamiTokenName = (typeof tokenNames)[number];",
    "",
    "export const tokenCssVariables: Record<JamiTokenName, string> = {",
    ...tokenNames.map((name) => `  ${JSON.stringify(name)}: ${JSON.stringify(`var(${tokenVarName(name)})`)},`),
    "};",
  ];
  return `${lines.join("\n")}\n`;
}

function toShadcnCssVars(tokenMap, schemaVersion) {
  const semantic = {};
  for (const mode of ["light", "dark"]) {
    semantic[mode] = {
      background: `var(${tokenVarName(`semantic.${mode}.background`)})`,
      foreground: `var(${tokenVarName(`semantic.${mode}.foreground`)})`,
      accent: `var(${tokenVarName(`semantic.${mode}.accent`)})`,
      ring: `var(${tokenVarName("componentState.focusRing")})`,
    };
  }
  return `${JSON.stringify(
    {
      $schema: "https://jami.studio/schemas/tokens/shadcn-css-vars.generated.json",
      schemaVersion,
      cssVars: semantic,
    },
    null,
    2,
  )}\n`;
}

function compareOrWrite(path, contents, { check }) {
  const absolutePath = join(root, path);
  if (check) {
    if (!existsSync(absolutePath)) return [`${path}: missing generated artifact`];
    const current = readFileSync(absolutePath, "utf8");
    return current === contents ? [] : [`${path}: generated artifact drift`];
  }
  mkdirSync(dirname(absolutePath), { recursive: true });
  writeFileSync(absolutePath, contents);
  return [];
}

export function registrySourceHash(item) {
  const normalized = structuredClone(item);
  if (normalized.lifecycle) normalized.lifecycle.sourceHash = "";
  return `sha256:${createHash("sha256").update(`${JSON.stringify(normalized, null, 2)}\n`).digest("hex")}`;
}

function registryOutputItem(item) {
  return {
    name: item.name.replace("@jami-studio/", ""),
    type: item.type === "primitive" ? "registry:ui" : `registry:${item.type}`,
    title: item.title,
    description: item.description,
    dependencies: item.dependencies,
    registryDependencies: [],
    files: item.files.map((file) => ({
      path: file.path,
      target: file.target,
      type: file.kind,
    })),
    meta: {
      packageName: item.name,
      suite: item.suite ?? null,
      lifecycle: item.lifecycle,
      tokenRequirements: item.tokenRequirements,
      provenance: item.provenance,
      compatibility: item.compatibility,
      agentManifest: item.agentManifest ?? null,
    },
  };
}

function generateTokenArtifacts({ check }) {
  const source = readJson(tokenSourcePath);
  const tokenMap = flattenTokens(source.tokens);
  const schemaVersion = source.$extensions["studio-ui"].schemaVersion;
  const outputs = source.$extensions["studio-ui"].generatedOutputs;

  return [
    ...compareOrWrite(outputs.cssVariables, toCssVariables(tokenMap), { check }),
    ...compareOrWrite(outputs.tailwindTheme, toTailwindTheme(tokenMap), { check }),
    ...compareOrWrite(outputs.typescriptTypes, toTypescriptTypes(tokenMap, schemaVersion), { check }),
    ...compareOrWrite(outputs.shadcnCssVars, toShadcnCssVars(tokenMap, schemaVersion), { check }),
  ];
}

function generateRegistryArtifacts({ check }) {
  const itemPaths = ["button.registry-item.json"];
  const sourceItems = itemPaths.map((name) => readJson(`${registrySourceDir}/${name}`));
  const generatedItems = sourceItems.map(registryOutputItem);
  const registry = {
    $schema: "https://ui.shadcn.com/schema/registry.json",
    name: "@jami-studio/registry",
    homepage: "https://registry.jami.studio",
    items: generatedItems,
  };
  const failures = compareOrWrite("packages/registry/generated/registry.json", `${JSON.stringify(registry, null, 2)}\n`, {
    check,
  });

  for (const item of generatedItems) {
    failures.push(
      ...compareOrWrite(
        `packages/registry/generated/items/${item.name}.registry-item.json`,
        `${JSON.stringify({ $schema: "https://ui.shadcn.com/schema/registry-item.json", ...item }, null, 2)}\n`,
        { check },
      ),
    );
  }
  return failures;
}

export function generateAllArtifacts({ check = false } = {}) {
  return [...generateTokenArtifacts({ check }), ...generateRegistryArtifacts({ check })];
}

const isDirectRun = process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1];

if (isDirectRun) {
  const check = process.argv.includes("--check");
  const failures = generateAllArtifacts({ check });
  if (failures.length > 0) {
    console.error(check ? "contracts:artifacts:check failed" : "contracts:artifacts:generate failed");
    for (const failure of failures) console.error(`- ${failure}`);
    process.exit(1);
  }
  console.log(check ? "contracts:artifacts:check passed" : "contracts:artifacts:generate wrote artifacts");
}
